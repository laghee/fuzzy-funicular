weather = 'foggy'

case weather
when 'sunny'
	puts "Don't forget your sunscreen!"
when 'cloudy'
	puts "At least it'll be a good sunset!"
when 'foggy'
	puts "Layers are key!"
when 'rainy'
	puts "Bring an umbrella, missy!"
when 'snowing'
	puts "Forget going out -- just hole up with a book!"
when 'windy'
	puts "Keep a scarf and hair clips handy!"
else 
    puts "Huh. Stumper. Good luck!!"
end